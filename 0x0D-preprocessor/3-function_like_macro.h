#ifndef FUNCTION_LIKE_MACRO_H
#define FUNCTION_LIKE_MACRO_H

/**
 * File: 3-function_like_macro.h
 *
 * Desc: Heder file that defines a function like macro named
 * ABS(x) that computes the absolute value of a number x.
 *
 * @x: The number whose absolute value is going to be computed.
 *
 * Author: Gizachew Bayness (eleccrazy).
 *
 * Date Created: May 8, 2022
 *
 */

#define ABS(x) ((x < 0) ? (-x) : (x))

#endif
