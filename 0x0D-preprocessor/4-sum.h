#ifndef SUM_H
#define SUM_H

/**
 * File: 0x0D-preprocessor
 *
 * Desc: This heder file defines a function like macro named
 * SUM(x, y) that computes the sum of the numbers x and y.
 *
 * @x: The first number to be added.
 * @y: The second number to be added.
 *
 * Author: Gizachew Bayness (eleccrazy)
 *
 * Date Created: May 9, 2022
 *
 */

#define SUM(x, y) ((x) + (y))

#endif
